def add_section(doc, title, subsections):
    """Add a section with subsections to the document"""
    # Add main section heading
    heading = doc.add_heading(title, level=1)
    heading.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    
    for subsection_title, content in subsections:
        # Add subsection heading
        subheading = doc.add_heading(subsection_title, level=2)
        subheading.alignment = WD_ALIGN_PARAGRAPH.RIGHT
        
        # Add content
        if isinstance(content, list):
            for item in content:
                if item:  # Skip empty strings
                    p = doc.add_paragraph(item)
                    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
                    # Set font properties for better Arabic rendering
                    for run in p.runs:
                        run.font.name = 'Arial'
                        run.font.size = Pt(11)
                else:
                    doc.add_paragraph()  # Empty line
        else:
            p = doc.add_paragraph(content)
            p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
            for run in p.runs:
                run.font.name = 'Arial'
                run.font.size = Pt(11)

